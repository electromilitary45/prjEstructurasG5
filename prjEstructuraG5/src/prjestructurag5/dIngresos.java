
package prjestructurag5;

public class dIngresos {
    private int ingresosDia;
    private String fecha;
    private double plataIngresos;
    
    public dIngresos(){
    this.ingresosDia=0;
    this.fecha="";
    this.plataIngresos=0;
    }

    public int getIngresosDia() {
        return ingresosDia;
    }

    public void setIngresosDia(int ingresosDia) {
        this.ingresosDia = ingresosDia;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public double getPlataIngresos() {
        return plataIngresos;
    }

    public void setPlataIngresos(double plataIngresos) {
        this.plataIngresos = plataIngresos;
    }
    
    
    
    
}
